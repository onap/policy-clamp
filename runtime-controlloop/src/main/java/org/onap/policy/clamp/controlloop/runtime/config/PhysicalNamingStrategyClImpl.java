package org.onap.policy.clamp.controlloop.runtime.config;

import java.io.Serializable;
import org.hibernate.boot.model.naming.Identifier;
import org.hibernate.boot.model.naming.PhysicalNamingStrategy;
import org.hibernate.engine.jdbc.env.spi.JdbcEnvironment;

public class PhysicalNamingStrategyClImpl implements PhysicalNamingStrategy, Serializable {
    private static final long serialVersionUID = 1L;

    private static final String[] COLLECTIONS =
        {"properties", "metadata", "constraints", "occurrences", "inputs", "attributes", "targets", "triggers"};

    @Override
    public Identifier toPhysicalCatalogName(Identifier name, JdbcEnvironment context) {
        return name;
    }

    @Override
    public Identifier toPhysicalSchemaName(Identifier name, JdbcEnvironment context) {
        return name;
    }

    @Override
    public Identifier toPhysicalTableName(Identifier name, JdbcEnvironment context) {
        for (var str : COLLECTIONS) {
            if (name.getText().endsWith("_" + str)) {
                String tableName = toUpper(name.getText(), str.toUpperCase());
                return Identifier.toIdentifier(tableName);
            }
        }
        return name;
    }

    private String toUpper(String name, String minus) {
        return name.substring(0, name.length() - minus.length()) + minus;
    }

    @Override
    public Identifier toPhysicalSequenceName(Identifier name, JdbcEnvironment context) {
        return name;
    }

    @Override
    public Identifier toPhysicalColumnName(Identifier name, JdbcEnvironment context) {
        return name;
    }
}
